// @lc code=start
/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public boolean isValidBST(TreeNode root) {
        return isValidBST(root.left, root.val, Long.MIN_VALUE)
            && isValidBST(root.right, Long.MAX_VALUE, root.val);
    } 

    public boolean isValidBST(TreeNode node, long max, long min) {
        if (node == null) {
            return true;
        }
        if(node.val <= min || node.val >= max) {
            return false;
        }
        return isValidBST(node.left, node.val, min)
            && isValidBST(node.right, max, node.val);
    }
}
